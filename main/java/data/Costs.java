package data;

/**
 * Class representing the costs of something.
 * 
 * @author Bastian Lang
 *
 */
public class Costs {

	private int cristal;
	private int gas;

	public int getCristal() {
		return cristal;
	}

	public void setCristal(int cristal) {
		this.cristal = cristal;
	}

	public int getGas() {
		return gas;
	}

	public void setGas(int gas) {
		this.gas = gas;
	}

}
